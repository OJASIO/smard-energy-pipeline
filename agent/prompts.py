INTENT_PROMPT = """Classify the user's question into exactly one of these categories.
Return ONLY the category name, nothing else.

Categories:
- FORECAST: questions about future renewable energy generation predictions or outlook
- ANOMALIES: questions about unusual events, anomalies, spikes, drops or unexpected grid behaviour
- RENEWABLE_SHARE: questions about what percentage or proportion of energy came from renewables
- DEMAND: questions about electricity consumption, demand levels, or how much power was used
- GENERATION: questions about how much energy was generated by specific sources like wind, solar, coal
- COMPARISON: questions explicitly comparing renewable metrics across different years or time periods (e.g. "compare this year vs last year", "year over year", "how does X compare to last year")
- GREETING: greetings, capability questions, or general help requests
- UNKNOWN: cannot be classified into any of the above

Important rules:
- "How has demand changed" or "electricity demand" → DEMAND (not COMPARISON)
- "Compare renewable share this year vs last year" → COMPARISON
- "What was renewable share last month" → RENEWABLE_SHARE (not COMPARISON)

User question: {question}

Category:"""


# ---------------------------------------------------------------------------
# Context formatter — converts DataFrame to clean text for LLM prompt
# ---------------------------------------------------------------------------
def format_context(df, template_name: str) -> str:
    if df is None or df.empty:
        return "No data available for this query."

    display_df = df.head(30)

    if template_name == "FORECAST":
        lines = ["Renewable generation forecast (MWh/day):"]
        for _, row in display_df.iterrows():
            lines.append(
                f"  {row.get('date', 'N/A')}: "
                f"predicted={float(row.get('predicted_mwh', 0)):,.0f} MWh, "
                f"range=[{float(row.get('lower_mwh', 0)):,.0f} – "
                f"{float(row.get('upper_mwh', 0)):,.0f}]"
            )
        return "\n".join(lines)

    elif template_name == "ANOMALIES":
        if display_df.empty:
            return "No anomalies detected in the requested period."
        lines = [f"Anomalies detected ({len(df)} total):"]
        for _, row in display_df.iterrows():
            lines.append(
                f"  {row.get('date', 'N/A')}: {row.get('type', 'N/A')} "
                f"({row.get('metric', 'N/A')}), severity={row.get('severity', 'N/A')}, "
                f"actual={float(row.get('actual_mwh', 0)):,.0f} MWh vs "
                f"expected={float(row.get('expected_mwh', 0)):,.0f} MWh"
            )
        return "\n".join(lines)

    elif template_name == "RENEWABLE_SHARE":
        min_date = display_df["date"].min() if "date" in display_df.columns else "N/A"
        max_date = display_df["date"].max() if "date" in display_df.columns else "N/A"
        lines = [f"Daily renewable energy share (data covers {min_date} to {max_date}):"]
        for _, row in display_df.iterrows():
            lines.append(
                f"  {row.get('date', 'N/A')}: {float(row.get('renewable_pct', 0)):.1f}% renewable "
                f"({float(row.get('renewable_mwh', 0)):,.0f} of "
                f"{float(row.get('total_mwh', 0)):,.0f} MWh total)"
            )
        avg_pct = df["renewable_pct"].mean() if "renewable_pct" in df.columns else 0
        lines.append(f"Average renewable share over this period: {float(avg_pct):.1f}%")
        return "\n".join(lines)

    elif template_name == "DEMAND":
        lines = ["Daily electricity demand (MWh):"]
        for _, row in display_df.iterrows():
            lines.append(
                f"  {row.get('date', 'N/A')}: {float(row.get('daily_demand_mwh', 0)):,.0f} MWh"
            )
        avg = df["daily_demand_mwh"].mean() if "daily_demand_mwh" in df.columns else 0
        lines.append(f"Period average: {float(avg):,.0f} MWh/day")
        return "\n".join(lines)

    elif template_name == "GENERATION":
        lines = ["Generation by energy source (total MWh, last period):"]
        for _, row in display_df.iterrows():
            renewable_tag = " [renewable]" if row.get("is_renewable") else ""
            lines.append(
                f"  {row.get('source', 'N/A')}{renewable_tag}: "
                f"{float(row.get('total_mwh', 0)):,.0f} MWh total, "
                f"{float(row.get('avg_mwh_per_period', 0)):,.1f} MWh avg per period"
            )
        return "\n".join(lines)

    elif template_name == "COMPARISON":
        if display_df.empty:
            return "No comparison data available."
        lines = ["Monthly average renewable share % — this year vs last year:"]
        for _, row in display_df.iterrows():
            try:
                yr = int(float(row.get("year", 0)))
                mo = int(float(row.get("month", 0)))
                pct = float(row.get("avg_renewable_pct", 0))
                mwh = float(row.get("avg_renewable_mwh", 0))
                lines.append(f"  {yr}-{mo:02d}: {pct:.1f}% renewable ({mwh:,.0f} MWh avg/day)")
            except Exception:
                continue
        if len(lines) == 1:
            return "No comparison data available."
        # Add summary
        df_2025 = display_df[display_df["year"].astype(str).str.contains("2025")]
        df_2026 = display_df[display_df["year"].astype(str).str.contains("2026")]
        if not df_2025.empty and not df_2026.empty:
            avg_2025 = float(df_2025["avg_renewable_pct"].mean())
            avg_2026 = float(df_2026["avg_renewable_pct"].mean())
            trend = "higher" if avg_2026 > avg_2025 else "lower"
            lines.append(f"Summary: 2026 avg {avg_2026:.1f}% vs 2025 avg {avg_2025:.1f}% — renewable share is {trend} this year.")
        return "\n".join(lines)

    else:
        return display_df.to_string(index=False)
